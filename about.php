		<h4>About</h4>
		<p>The Global Foundation For Children With Hearing Loss was born out of a journey across Southeast Asia in 2008. I saw that limited professional expertise, shortage of hearing aids, and general lack of awareness about hearing loss was having a detrimental effect on the livelihoods of thousands of children who are deaf and hard of hearing.  It was a stark contrast to my personal experience growing up with hearing loss in the United States where professional support and resources are in greater supply.</p>

		<p>I was born profoundly hard of hearing but communicate through listening and spoken language. I was identified with hearing loss and fit with hearing aids early in life and provided with the early intervention support necessary to learn to listen and talk.  I was mainstreamed in kindergarten and went to neighborhood schools alongside my hearing peers for the rest of my academic career.  After earning a Masters degree, I embarked on a marketing career at Fortune 500 companies.  I know that the success I have had would not have been possible without the support that I received in my earliest years.</p>   

		<p>Three elements are equally essential for young children with hearing loss to learn to listen and talk: early identification, access to hearing technology, and the support of locally trained professionals who can help these children process sounds they were not born hearing to develop listening and spoken language. In much of the developing world, these three elements do not exist.  I started the Global Foundation For Children With Hearing Loss in 2009 to help infants and young children with hearing loss in low and middle income countries have access to the resources that I was fortunate to have.</p>

		<p>The Global Foundation For Children With Hearing Loss is supported by a board of directors and an international team of talented audiologists, speech therapists, early interventionists, and auditory-verbal deaf education professionals.  We are working together with local partners in the global community to identify and address gaps in the system of support across healthcare and education for young children with hearing loss.  We provide training programs, hearing technology, awareness campaigns, and help develop and implement systems and processes designed to directly benefit thousands of deaf and hard of hearing children and their families. I invite you to watch the 3-minute video below for more about our work.</p>

		<p>We have been working in Vietnam since 2010 and are expanding our successful model to other countries who have requested our expertise.</p>

		<p>I never fail to get energized by what we are doing and our powerful mission. If you would like to join us in making a significant difference in the futures of children around the world who are deaf and hard of hearing, we would be honored to have your support.</p>

